    private void tbNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNhanVienMouseClicked
        ProcessCrt1(true);
        this.btLuu.setEnabled(false);
        try{
            int row = this.tbNhanVien.getSelectedRow();
            String IDrow = (this.tbNhanVien.getModel().getValueAt(row, 0)).toString();
            String sql1 = "SELECT * FROM nhanvien where Ma_NV='"+IDrow+"'";
            ResultSet rs = Bang.show(sql1);
            if(rs.next()) {
                this.jTextField1.setText(rs.getString("Ma_NV"));
                this.jTextField2.setText(rs.getString("Ten_NV"));
                this.jTextField3.setText(rs.getString("SDT_NV"));
                this.jTextField4.setText(rs.getString("Chuc_vu"));
                this.jTextField6.setText(rs.getString("Dc_NV"));
                
            }
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Lỗi" , "Message", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tbNhanVienMouseClicked

